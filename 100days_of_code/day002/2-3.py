# ARREDONDANDO PRECISAMENTE
print("Ao converter para inteiro se perde as casas decimais")
print(f"Valor em Float : {8/3}")
print(f"Valor arredondado para o valor inteiro abaixo (floor) {8//3}")
print(f"Valor convertido diretamente para Int : {int(8/3)}")
print(f"Valor Arredondado sem especificar numero de casas decimais : {round(8/3)}")
print(f"Valor arredondado com 1 casa decimal {round(8/3,1)}")
print(f"Valor arredondado com 2 casas decimais {round(8/3,2)}")
print(f"Valor arredondado com 3 casas decimais {round(8/3,3)}")
